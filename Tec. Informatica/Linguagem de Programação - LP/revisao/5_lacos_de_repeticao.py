#   1-  Repetição   FOR
#Usado para repetir um número conhecido de vezes.

for i in range(5):
    print("Olá", i)

#Literado sobre uma lista (veremos lista mais abaixo)
frutas = ["maçã", "banana", "uva"]
for fruta in frutas:
    print(fruta)

#   2-  Repetição   While
#Repete enquanto a condição for verdadeira.

contador = 1
while contador <= 5:
    print(contador)
    contador += 1
    